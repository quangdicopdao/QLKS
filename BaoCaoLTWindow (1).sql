CREATE PROC [dbo].[SP_AuthoLogin]
@Username nvarchar(20),
@Password nvarchar(20)
as
begin
    if exists (select * from THANHVIEN where TENDANGNHAP = @Username and MatKhau = @Password and PHANQUYEN = N'CHỦ KHÁCH SẠN')
        select 0 as code
    else if exists (select * from Thanhvien where TENDANGNHAP = @Username and MatKhau = @Password and PHANQUYEN = N'QUẢN LÝ')
        select 1 as code
	else if exists (select * from Thanhvien where TENDANGNHAP = @Username and MatKhau = @Password and PHANQUYEN = N'NHÂN VIÊN')
        select 2 as code
    else if exists(select * from Thanhvien where TENDANGNHAP = @Username and MatKhau != @Password) 
        select 3 as code
    else select 4 as code
end

GO
