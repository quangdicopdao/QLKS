CREATE PROC [dbo].[SP_DoiMatKhau]
@User nvarchar(50),
@OldPass nvarchar(50),
@NewPass nvarchar(50)
as
begin
    if exists (select * from THANHVIEN where TENDANGNHAP = @User and MATKHAU = @OldPass)
    begin
        update THANHVIEN set MatKhau = @NewPass where TENDANGNHAP = @User
        select 1 as code, N'Thay doi mat khau thanh cong !!' as MSG
    end
    else
        select 0 as code, N'Tai khoan hoac mat khau sai !!' as MSG
end

